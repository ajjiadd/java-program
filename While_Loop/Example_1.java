package While_Loop;

public class Example_1 {
    public static void main(String[] args){

        //example: 01

        int x = 1;
        while(x < 6){
            System.out.println(x);
            x++;
        }

        System.out.println("\n");
        //example: 02

        int i = 0;
        do{
            System.out.println(i);
            i++;
        }while(i <= 10);
    }
}
