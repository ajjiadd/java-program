package java_string;

public class concatenation {
    public static void main(String[] args){
        //way number 01:
        String firstname = "Abdullah";
        String lastname = "Al-jiad"; 
        System.out.println(firstname +" "+ lastname + "\n");
    
        //way number 02:
        String first_name = "Abdullah";
        String last_name = "Al-jiad";
        System.out.println(first_name.concat(last_name) + "\n"); 
    }
}
