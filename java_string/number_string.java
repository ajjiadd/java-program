package java_string;

public class number_string {
    public static void main(String[] args){
        int x = 15;
        int y = 15;
        int result = x + y; //result  will be 30 (an integer/number)
        System.out.println(result + "\n");


        int a = 15;
        int b = 15;
        float result_2 = a + b; //result  will be 30.0 (a float/number)
        System.out.println(result_2 + "\n");


        String p = "10";
        int q = 20;
        String z = p + q;  // z will be 1020 (a String)
        System.out.println(z);
    }
}
