package java_string;

public class string_xm_1 {
    public static void main(String[] args){
        //String
        String x = "jiad";
        System.out.println(x + "\n");

        //String length
        String y = "HELLOWORLD";
        System.out.println(y.length() + "\n");

        //More String Methods{toUpperCase()}
        String z = "Hello, Jiad";
        System.out.println(z.toUpperCase() + "\n");

        //More String Methods{toLowerCase()}

        String a = "Hello, Brothers";
        System.out.println(a.toLowerCase() + "\n" );

        //Finding a Character in a String
        String text = "Please locate where 'locate' occurs!";
        System.out.println(text.indexOf("locate"));

    }
}
